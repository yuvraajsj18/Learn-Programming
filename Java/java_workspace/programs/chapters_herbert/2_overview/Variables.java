// using variables in java

class Variables
{
    public static void main(String args[])
    {
        int main = 1;   // main can be used for variable name it is not a keyword
        int num = 50 + main;
        // num = 50;
        System.out.println("The value of num is " + num);
        num *= 2; // num = num * 2;
        System.out.print("The value of num * 2 is " + num + '\n');
        // System.out.println(num);
    }
}