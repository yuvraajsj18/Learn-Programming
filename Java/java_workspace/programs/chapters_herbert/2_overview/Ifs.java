// use if in java

class Ifs
{
    public static void main(String args[])
    {
        int x = 50, y = 100;
        if (x < y)
            System.out.println("x < y");
        
        x = x * 2;
        if (x == y)
            System.out.println("x == y");
        
        x = x * 2;
        if (x > y)
            System.out.println("x > y");

        if (x == y)
            System.out.println("x == y");
    }
}